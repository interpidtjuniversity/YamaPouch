package main

import (
	_ "./nsexecute"
	log "github.com/Sirupsen/logrus"
	"os"
	"os/exec"
	"strings"
)

const ENV_EXEC_EXECUTE_CMD = "mydocker_execute_cmd"

func ExecuteCommandInContainer(containerName string, comArray []string) {
	pid, err := GetContainerPidByName(containerName)
	if err != nil {
		log.Errorf("Exec container getContainerPidByName %s error %v", containerName, err)
		return
	}

	cmdStr := strings.Join(comArray, " ")
	log.Infof("container pid %s", pid)
	log.Infof("command %s", cmdStr)

	cmd := exec.Command("/proc/self/exe", "execute")
	cmd.Stdin = os.Stdin
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	os.Setenv(ENV_EXEC_PID, pid)
	os.Setenv(ENV_EXEC_EXECUTE_CMD, cmdStr)

	containerEnvs := getEnvsByPid(pid)
	cmd.Env = append(os.Environ(), containerEnvs...)

	if err := cmd.Run(); err != nil {
		log.Errorf("execute in container %s error %v", containerName, err)
	}
}


